package com.nh.dataspider.missevan.model;

public class MissEvanDrama {
	
	private String dramaName;
	
	private String id;

	private String name;
	
	private String drama_id;
	
	private String sound_id;
	
	private String data;
	
	private int order;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getDrama_id() {
		return drama_id;
	}

	public void setDrama_id(String drama_id) {
		this.drama_id = drama_id;
	}

	public String getSound_id() {
		return sound_id;
	}

	public void setSound_id(String sound_id) {
		this.sound_id = sound_id;
	}

	public String getData() {
		return data;
	}

	public void setData(String data) {
		this.data = data;
	}

	public int getOrder() {
		return order;
	}

	public void setOrder(int order) {
		this.order = order;
	}

	public String getDramaName() {
		return dramaName;
	}

	public void setDramaName(String dramaName) {
		this.dramaName = dramaName;
	}
	
}
