package com.nh.dataspider.missevan.model;

public class MissEvanSound {
	
	private String id;

	private String front_cover;
	
	private String intro;
	
	private String soundstr;
	
	private String soundurl;
	
	private String videourl;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getFront_cover() {
		return front_cover;
	}

	public void setFront_cover(String front_cover) {
		this.front_cover = front_cover;
	}

	public String getIntro() {
		return intro;
	}

	public void setIntro(String intro) {
		this.intro = intro;
	}

	public String getSoundstr() {
		return soundstr;
	}

	public void setSoundstr(String soundstr) {
		this.soundstr = soundstr;
	}

	public String getSoundurl() {
		return soundurl;
	}

	public void setSoundurl(String soundurl) {
		this.soundurl = soundurl;
	}

	public String getVideourl() {
		return videourl;
	}

	public void setVideourl(String videourl) {
		this.videourl = videourl;
	}
	
}
